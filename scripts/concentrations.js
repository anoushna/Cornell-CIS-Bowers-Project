//CONCENTRATION 1
$("#data-science-concentration").on("click", function() {
    if ($("#data-science-concentration-box").hasClass("accessible-hidden")) {
      $("#data-science-concentration-box").removeClass("accessible-hidden");
    } else {
      $("#data-science-concentration-box").addClass("accessible-hidden");
    }
});

